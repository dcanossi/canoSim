#include "vec.h"

vec::vec(const float& x, const float& y, const float& z)
:
    vecX_(x),
    vecY_(y),
    vecZ_(z)
{}

std::ostream& operator<<(std::ostream& os, const vec& vector)
{
    os << "("
        << vector.vecX_ << " " << vector.vecY_ << " " << vector.vecZ_
        << ")";

    return os;
}